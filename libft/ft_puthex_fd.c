#include <unistd.h>

void	ft_puthex_fd(const unsigned char c, int fd)
{
	int		i;
	int		j;
	char	hex[2];

	j = c % 16;
	i = c / 16;
	hex[0] = i + '0' * (i <= 9) + ('A' - 10) * (i > 9);
	hex[1] = j + '0' * (j <= 9) + ('A' - 10) * (j > 9);
	write(fd, hex, 2);
}
